package cf.phoenixfirewingz.remapper.utils;

public class StringTuple
{
	private final String forge, fabric;

	public StringTuple(String forgeIn, String fabricIn)
	{
		forge = forgeIn;
		fabric = fabricIn;
	}

	public String getForge()
	{
		return forge;
	}

	public String getFabric()
	{
		return fabric;
	}
}
